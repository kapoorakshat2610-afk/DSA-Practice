bool isPowerOfFour(int n) {
    return n == (1 << 0)  ||  // 4^0 = 1
           n == (1 << 2)  ||  // 4^1 = 4
           n == (1 << 4)  ||  // 4^2 = 16
           n == (1 << 6)  ||  // 4^3 = 64
           n == (1 << 8)  ||  // 4^4 = 256
           n == (1 << 10) ||  // 4^5 = 1024
           n == (1 << 12) ||
           n == (1 << 14) ||
           n == (1 << 16) ||
           n == (1 << 18) ||
           n == (1 << 20) ||
           n == (1 << 22) ||
           n == (1 << 24) ||
           n == (1 << 26) ||
           n == (1 << 28) ||
           n == (1 << 30);    // 4^15 = 1073741824
}
